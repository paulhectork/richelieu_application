    <p>Les constructions anciennes toujours présentes dans
      la topographie parisienne varient en nombre et en nature,
      selon qu'elles appartiennent à un passé lointain ou
      plus récent, et en fonction de leur emplacement dans
      la ville. Le processus de patrimonialisation de Paris,
      et plus particulièrement du quartier dit <q>&nbsp;Richelieu&nbsp;</q>,
      situé entre la place des Victoires, les Grands Boulevards
      et l'Opéra Garnier, s'est amorcé au début du XX<sup>e</sup>
      siècle et s'est poursuivi jusqu'aux années 2010. Les
      différentes phases de protection dans le quartier ont
      émergé en lien avec une prise de conscience accrue
      de la valeur historique des lieux
      <UiButtonEllipsis data-key="gigot"></UiButtonEllipsis>.
      Deux niveaux de
      protection doivent être distingués, dont le second
      est le plus élevé&nbsp;: l'inscription et le classement
      au titre des Monuments historiques. Dans le quartier
      Richelieu, la patrimonialisation peut s'illustrer à
      travers trois cas d'étude semi privés semi publics&nbsp;:
      le
      <RouterLink to="/entite-nommee/qr169af23423c6c4d5a944bcd1eaf284f12">
        Palais-Royal</RouterLink>,
      les bâtiments de la
      <RouterLink to="/institution/qr1e60d7b097d4c4992af50cd72c04cc201">
        Bibliothèque nationale de France</RouterLink>,
      et la
      <RouterLink to="/entite-nommee/qr18a6b2da1dc164c3a94de23e5fe6aa602">
        galerie Colbert</RouterLink>.
      La création de la Commission des monuments historiques
      en 1837, permet de faire émerger une véritable organisation
      qui se met en place pour <q>&nbsp;conserver ou remettre
      en bon état
      <UiButtonEllipsis data-key="circulaire"></UiButtonEllipsis>&nbsp;</q>
      le patrimoine, tel que le définit
      Aloïs Riegl dans <i>Le Culte moderne des monuments</i> en
      1903
      <UiButtonEllipsis data-key="riegl"></UiButtonEllipsis>.
    </p>
