      dispose de 35 protections au titre des Monuments historiques
      attribuées entre 1920 et 2015. Il s'agit de classements
      et d'inscriptions, des façades sur rue ou jardin, des
      toitures ou encore parfois des intérieurs. Au restaurant
      situé 17 rue Beaujolais, c'est le cas pour les deux
      salles du rez-de-chaussée avec leur décor et leur plafond
      peint ainsi que les deux panneaux peints remontés dans
      la salle de l'entresol. C'est principalement au cours
      des années 1920 que le quartier a reçu de nombreuses
      protections à l'image de l'hôtel Lully où se trouve
      actuellement le Centre allemand d'histoire de l'art&nbsp;
      <UiButtonEllipsis data-key="merimeeB"></UiButtonEllipsis>.
    </p>
    <p>Quant à la Bibliothèque nationale de France
      <UiButtonEllipsis data-key="merimeeC"></UiButtonEllipsis>&nbsp;
      <UiButtonEye data-key="qr1c7b58cbd909641c5b396f7c84df9c44e"></UiButtonEye>,
      située entre les rues Richelieu,
      Vivienne, Colbert et des Petits-Champs
      (XVII<sup>e</sup>-XX<sup>e</sup> siècles),
      elle se distingue comme l'un des bâtiments
      les plus protégés du quartier. À titre d'exemple, l'ensemble
      des façades et des toitures donnant sur rues, cours
      et jardins (à l'exception de la façade est de Robert
      de Cotte), le vestibule de la salle de lecture des
      manuscrits, ainsi que l'escalier du Cabinet des Estampes,
      ont été inscrits par arrêté le 29 septembre 1983. De
      plus, les galeries Mansart et Mazarine avec leur vestibule,
      ainsi que la salle de travail du Département des imprimés,
      connue sous le nom de salle Labrouste, ont été classés
      le 29 décembre 1983.
    </p>
    <p>Enfin, la galerie Colbert
      <UiButtonEye data-key="qr1d3e17ff1f1f1469c9434e8cc85a12d90"></UiButtonEye>,
      datant des années 1820 et située entre le 6 rue
      des Petits-Champs et les numéros 2 et 4 rue Vivienne,
      comprend également des parties protégées. En effet,
      la galerie elle-même, avec son décor, y compris la
      partie remaniée, notamment la rotonde, ainsi que les
      façades sur rue et sur cour des immeubles et les entrées,
      ont été inscrites par arrêté du 7 juillet 1974. Ces
      protections, d'un ensemble commercial, moins spectaculaire
      ou luxueux que les précédents, sont intervenues relativement
      tard en comparaison à d'autres édifices du quartier.
      Il est toutefois intéressant de noter que la galerie
      Colbert et sa voisine, la
      <RouterLink to="/entite-nommee/qr148a03a1991e648cca62a6c34bd36d6ae">
        galerie Vivienne</RouterLink>, ont été
      protégées simultanément, malgré le meilleur état de
      conservation de la seconde.
    </p>
    <p>À l'instar du quartier du Marais,
      qui, jusque dans les années 1960, était menacé en raison
      de son mauvais état de conservation
      <UiButtonEllipsis data-key="marais"></UiButtonEllipsis>,
      de nombreuses
      questions se posent&nbsp;: comment préserver sans figer&nbsp;?
      Comment éviter de transformer Paris en une ville musée&nbsp;?
    </p>
    <p>La réinterprétation du patrimoine et la reconversion
      des lieux, comme c'est le cas pour la
      <RouterLink to="/entite-nommee/qr1516df93d37614811870ccba88b4c6206">
        BnF Richelieu</RouterLink>,
      témoignent d'une ingéniosité particulière, mais aussi
      d'une volonté de <q>&nbsp;valorisation et de protection
      d'un bien symbole d'une catégorie de la société&nbsp;</q>
      et devient ainsi <q>&nbsp;l'enjeu de la conservation
      de la mémoire de cette même catégorie
      <UiButtonEllipsis data-key="papalardo"></UiButtonEllipsis>&nbsp;</q>.
      Le patrimoine occupe désormais une place prépondérante
      dans la fabrique de la ville contemporaine.
    </p>
  </div>

